function carregarDados([]) {

    tabela = document.getElementById("harmonico-table");

    tons.forEach(notas => {
        notas.forEach(nota => {
            // titulo
            const titulo = document.createElement('div');
            titulo.setAttribute('class', 'title');
            const text = document.createTextNode(nota.titulo)
            titulo.appendChild(text)

            // descricao
            const descricao = document.createElement('div');
            descricao.setAttribute('class', 'description');
            const text2 = document.createTextNode(nota.descricao)
            descricao.appendChild(text2)

            // 1
            const campoHarmonicoInner = document.createElement('div');
            campoHarmonicoInner.setAttribute('class', 'campo-harmonico-inner');
            campoHarmonicoInner.insertAdjacentElement('beforeend', titulo)
            campoHarmonicoInner.insertAdjacentElement('beforeend', descricao)

            // tipo de escala
            const campoHarmonico = document.createElement('div');
            campoHarmonico.setAttribute('class', 'campo-harmonico ' + nota.grau);
            // campoHarmonico.setAttribute('data-description', nota.dataDescricao);
            campoHarmonico.setAttribute('data-description', "nota.dataDescricao");
            
            const notaFormada = document.createElement('div');
            notaFormada.setAttribute('class', "notaSvg");
            this.formarNota("b",notaFormada);
            campoHarmonicoInner.appendChild(notaFormada);//svg-container





            // adicionar na tabela
            campoHarmonico.insertAdjacentElement('beforeend', campoHarmonicoInner)
            tabela.insertAdjacentElement('beforeend', campoHarmonico)
        })
    });
}

